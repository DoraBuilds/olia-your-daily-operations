/**
 * export-utils.ts
 * Real file download helpers using jsPDF + jspdf-autotable.
 * jsPDF and autoTable are loaded lazily (dynamic import) so they don't
 * bloat the initial bundle — they're only fetched when the user clicks Export.
 * Used by ReportingTab (PDF + CSV) and LogDetailModal (PDF).
 */

// ─── Brand colours (RGB) ─────────────────────────────────────────────────────
// Primary: Midnight Blue #1A2A47  · Background: Alabaster White #FDFAF7
const SAGE: [number, number, number] = [26, 42, 71];     // midnight blue #1A2A47
const OK: [number, number, number] = [58, 107, 79];      // forest green
const WARN: [number, number, number] = [196, 133, 74];   // warm amber
const ERR: [number, number, number] = [142, 47, 62];     // deep rose
const GRAY: [number, number, number] = [133, 123, 114];  // warm gray (muted-foreground)
const LIGHT_BG: [number, number, number] = [253, 250, 247]; // warm white (card surface)

function scoreRgb(score: number): [number, number, number] {
  return score >= 85 ? OK : score >= 65 ? WARN : ERR;
}

// ─── Reporting: PDF ──────────────────────────────────────────────────────────

export interface ReportingRow {
  checklist: string;
  location?: string;
  completedBy: string;
  date?: string;
  startedAt?: string;
  finishedAt?: string;
  score: number | null;
}

export interface ChecklistTemplateQuestion {
  text: string;
  required?: boolean;
}

export interface ChecklistTemplateSection {
  name?: string;
  questions: ChecklistTemplateQuestion[];
}

export interface ChecklistTemplateData {
  title: string;
  schedule?: string | null;
  timeOfDay?: string | null;
  sections: ChecklistTemplateSection[];
}

export async function exportReportingPdf(
  rows: ReportingRow[],
  periodLabel: string,
  stats: { completed: number; avg: number; open: number }
) {
  const { jsPDF } = await import("jspdf");
  const { default: autoTable } = await import("jspdf-autotable");

  const doc = new jsPDF({ orientation: "landscape", unit: "mm", format: "a4" });
  const pageW = doc.internal.pageSize.getWidth();
  let y = 18;

  // ── Brand header ──
  doc.setFont("helvetica", "bold");
  doc.setFontSize(13);
  doc.setTextColor(...SAGE);
  doc.text("Olia", 14, y);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(8.5);
  doc.setTextColor(...GRAY);
  doc.text(`Completion Logs  •  ${periodLabel}`, 14, y + 7);
  doc.text(
    `Generated ${new Date().toLocaleDateString("en-GB", { day: "numeric", month: "short", year: "numeric" })}`,
    pageW - 14,
    y + 7,
    { align: "right" }
  );

  y += 14;
  doc.setDrawColor(220, 220, 220);
  doc.line(14, y, pageW - 14, y);
  y += 9;

  // ── Stat cards ──
  const statItems = [
    { label: "Completed", value: String(stats.completed), color: [30, 30, 30] as [number, number, number] },
    { label: "Avg. score", value: stats.avg ? `${stats.avg}%` : "—", color: scoreRgb(stats.avg) },
    { label: "Open actions", value: String(stats.open), color: ERR },
  ];
  const colW = (pageW - 28) / 3;
  statItems.forEach((s, i) => {
    const cx = 14 + i * colW + colW / 2;
    doc.setFont("helvetica", "bold");
    doc.setFontSize(18);
    doc.setTextColor(...s.color);
    doc.text(s.value, cx, y, { align: "center" });
    doc.setFont("helvetica", "normal");
    doc.setFontSize(8);
    doc.setTextColor(...GRAY);
    doc.text(s.label, cx, y + 6, { align: "center" });
  });
  y += 16;

  // ── Table ──
  autoTable(doc, {
    startY: y,
    head: [["Checklist", "Location", "Completed by", "Start date/time", "End date/time", "Pass %"]],
    body: rows.map(r => [
      r.checklist,
      r.location ?? "—",
      r.completedBy,
      r.startedAt ?? r.date ?? "—",
      r.finishedAt ?? r.date ?? "—",
      r.score == null ? "—" : `${r.score}%`,
    ]),
    theme: "grid",
    headStyles: {
      fillColor: SAGE,
      textColor: [255, 255, 255],
      fontSize: 9,
      fontStyle: "bold",
    },
    bodyStyles: { fontSize: 8.5, textColor: [40, 40, 40] },
    alternateRowStyles: { fillColor: LIGHT_BG },
    columnStyles: {
      3: { halign: "center" },
      4: { halign: "center" },
      5: { halign: "center", fontStyle: "bold" },
    },
    didParseCell: (data: any) => {
      if (data.section === "body" && data.column.index === 5) {
        const score = rows[data.row.index]?.score;
        data.cell.styles.textColor = typeof score === "number" ? scoreRgb(score) : GRAY;
      }
    },
    margin: { left: 14, right: 14 },
  });

  const finalY: number = (doc as any).lastAutoTable?.finalY ?? y + 40;
  doc.setFontSize(7);
  doc.setTextColor(...GRAY);
  doc.text("Generated by Olia · Read-only report", pageW / 2, finalY + 10, { align: "center" });

  doc.save(`completion-logs-${periodLabel.replace(/[^a-zA-Z0-9]/g, "-")}.pdf`);
}

// ─── Reporting: CSV ──────────────────────────────────────────────────────────

export function exportReportingCsv(rows: ReportingRow[], periodLabel: string) {
  const headers = ["Checklist", "Completed by", "Date", "Score"];
  const body = rows.map(r => [r.checklist, r.completedBy, r.date, r.score == null ? "—" : `${r.score}%`]);
  const csv = [headers, ...body]
    .map(row => row.map(cell => `"${String(cell).replace(/"/g, '""')}"`).join(","))
    .join("\n");

  // BOM (\uFEFF) ensures Excel opens the file as UTF-8
  const blob = new Blob(["\uFEFF" + csv], { type: "text/csv;charset=utf-8;" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `completion-logs-${periodLabel.replace(/[^a-zA-Z0-9]/g, "-")}.csv`;
  a.click();
  URL.revokeObjectURL(url);
}

// ─── Checklist Template: PDF ────────────────────────────────────────────────

export async function exportChecklistTemplatePdf(checklist: ChecklistTemplateData) {
  const { jsPDF } = await import("jspdf");

  const doc = new jsPDF({ unit: "mm", format: "a4" });
  const margin = 20;
  let y = 30;

  doc.setFontSize(20);
  doc.setFont("helvetica", "bold");
  doc.text(checklist.title, margin, y);
  y += 8;

  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(120, 120, 120);
  const meta = [
    `${checklist.sections.flatMap(section => section.questions).length} questions`,
    checklist.schedule ? String(checklist.schedule) : null,
    checklist.timeOfDay && checklist.timeOfDay !== "anytime" ? checklist.timeOfDay : null,
  ].filter(Boolean).join("  ·  ");
  if (meta) {
    doc.text(meta, margin, y);
    y += 6;
  }
  doc.setTextColor(0, 0, 0);
  y += 4;

  doc.setDrawColor(220, 220, 220);
  doc.line(margin, y, 210 - margin, y);
  y += 8;

  let questionNumber = 0;
  checklist.sections.forEach((section) => {
    if (section.name) {
      doc.setFontSize(9);
      doc.setFont("helvetica", "bold");
      doc.setTextColor(100, 100, 100);
      doc.text(section.name.toUpperCase(), margin, y);
      y += 7;
      doc.setTextColor(0, 0, 0);
    }

    section.questions.forEach((question) => {
      if (y > 270) {
        doc.addPage();
        y = 20;
      }

      questionNumber += 1;
      doc.setFontSize(10);
      doc.setFont("helvetica", "normal");
      const label = `${questionNumber}. ${question.text || `Question ${questionNumber}`}${question.required ? " *" : ""}`;
      const lines = doc.splitTextToSize(label, 170 - margin);
      doc.text(lines, margin, y);
      y += lines.length * 6 + 2;

      doc.setDrawColor(200, 200, 200);
      doc.roundedRect(margin, y, 170 - margin, 8, 1, 1);
      y += 13;
    });

    y += 2;
  });

  doc.setFontSize(8);
  doc.setTextColor(160, 160, 160);
  doc.text(`Generated by Olia · ${new Date().toLocaleDateString()}`, margin, 287);
  doc.save(`${checklist.title.replace(/[^a-z0-9]/gi, "_")}.pdf`);
}

// ─── Log Detail: PDF ─────────────────────────────────────────────────────────

export interface LogDetailData {
  checklist: string;
  completedBy: string;
  date: string;
  score: number | null;
  startedAt?: string;   // HH:MM — optional (not stored in DB for older logs)
  finishedAt?: string;  // HH:MM — optional (derived from created_at or passed directly)
  answers?: Array<{
    label: string;
    type: string;
    required: boolean;
    answer?: string;
    hasPhoto?: boolean;
    comment?: string;
  }>;
}

export async function exportLogDetailPdf(log: LogDetailData) {
  const { jsPDF } = await import("jspdf");
  const { default: autoTable } = await import("jspdf-autotable");

  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
  const pageW = doc.internal.pageSize.getWidth();
  let y = 18;

  // ── Brand header ──
  doc.setFont("helvetica", "bold");
  doc.setFontSize(13);
  doc.setTextColor(...SAGE);
  doc.text("Olia", 14, y);

  // ── Checklist title ──
  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.setTextColor(30, 30, 30);
  doc.text(log.checklist, 14, y + 9);

  // ── Meta ──
  doc.setFont("helvetica", "normal");
  doc.setFontSize(8.5);
  doc.setTextColor(...GRAY);
  const timingParts: string[] = [];
  if (log.startedAt) timingParts.push(`Started ${log.startedAt}`);
  if (log.finishedAt) timingParts.push(`Finished ${log.finishedAt}`);
  const timingStr = timingParts.length ? `  ·  ${timingParts.join("  ·  ")}` : "";
  doc.text(`${log.completedBy}  ·  ${log.date}${timingStr}`, 14, y + 16);

  // ── Score (top-right) ──
  doc.setFont("helvetica", "bold");
  doc.setFontSize(16);
  doc.setTextColor(...(typeof log.score === "number" ? scoreRgb(log.score) : GRAY));
  doc.text(log.score == null ? "—" : `${log.score}%`, pageW - 14, y + 11, { align: "right" });
  doc.setFont("helvetica", "normal");
  doc.setFontSize(7.5);
  doc.setTextColor(...GRAY);
  doc.text("Score", pageW - 14, y + 17, { align: "right" });

  y += 23;
  doc.setDrawColor(220, 220, 220);
  doc.line(14, y, pageW - 14, y);
  y += 6;

  // ── Answers table — columns: Task | Answer (Required column removed per spec) ──
  const answers = log.answers ?? [];
  const tableBody = answers.map(ans => {
    let response = "—";
    if (ans.type === "checkbox") {
      response = ans.answer === "true" || ans.answer === "yes" ? "✓  Completed" : "✗  Not completed";
    } else if ((ans.type === "numeric" || ans.type === "number") && ans.answer) {
      response = ans.answer;
    } else if (ans.type === "photo" || ans.type === "media") {
      // Store base64 data URL in DB — never print it; just show a human-readable label
      const hasPhoto = ans.hasPhoto || (typeof ans.answer === "string" && ans.answer.startsWith("data:image"));
      response = hasPhoto ? "📷 Photo attached" : "No photo";
    } else if (ans.answer) {
      response = String(ans.answer);
    }
    const full = ans.comment ? `${response}\n↳ ${ans.comment}` : response;
    return [ans.label, full];
  });

  autoTable(doc, {
    startY: y,
    head: [["Task", "Answer"]],
    body: tableBody,
    theme: "grid",
    headStyles: {
      fillColor: SAGE,
      textColor: [255, 255, 255],
      fontSize: 9,
      fontStyle: "bold",
    },
    bodyStyles: { fontSize: 8.5, textColor: [40, 40, 40] },
    alternateRowStyles: { fillColor: LIGHT_BG },
    columnStyles: {
      0: { cellWidth: 70 },
      1: { cellWidth: "auto" },
    },
    didParseCell: (data: any) => {
      if (data.section === "body" && data.column.index === 1) {
        const ans = answers[data.row.index];
        if (!ans) return;
        const answered =
          ans.answer === "true" || ans.answer === "yes" ||
          ((ans.type === "numeric" || ans.type === "number") && !!ans.answer) ||
          ans.hasPhoto;
        if (ans.required && !answered) {
          data.cell.styles.textColor = ERR;
        } else if (answered) {
          data.cell.styles.textColor = OK;
        }
      }
    },
    margin: { left: 14, right: 14 },
  });

  const finalY: number = (doc as any).lastAutoTable?.finalY ?? y + 40;
  doc.setFontSize(7);
  doc.setTextColor(...GRAY);
  doc.text(
    "Generated by Olia  ·  Read-only report  ·  Responses cannot be edited",
    pageW / 2,
    finalY + 10,
    { align: "center" }
  );

  const safeName = log.checklist.replace(/[^a-zA-Z0-9\s]/g, "").replace(/\s+/g, "-");
  doc.save(`${safeName}-report.pdf`);
}
